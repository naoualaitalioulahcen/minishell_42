#include "minishell.h"

int	check_first_c(char c)
{
	if ((c >= 'a' && c <= 'z') || \
		(c >= 'A' && c <= 'Z') || c == '_')
		return (1);
	return (0);
}

int	check_first_c2(char c)
{
	if ((c >= 'a' && c <= 'z') || \
		(c >= 'A' && c <= 'Z') || c == '_' || (c >= '0' && c <= '9'))
		return (1);
	return (0);
}
